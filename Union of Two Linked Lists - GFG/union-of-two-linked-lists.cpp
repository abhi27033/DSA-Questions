class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        // code here
        set<int> s;
        struct Node* h=head1;
        while(h)
        {
            s.insert(h->data);
            h=h->next;
        }
        h=head2;
        while(h)
        {
            s.insert(h->data);
            h=h->next;
        }
        struct Node*q=new Node(0);
        struct Node*ans=q;
        for(auto it:s)
        {
           q->next=new Node(it);
           q=q->next;
        }
        q->next=NULL;
        struct Node *t=ans;
        ans=ans->next;
        delete(t);
        return ans;
    }
};


