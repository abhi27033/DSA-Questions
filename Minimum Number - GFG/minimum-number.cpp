class Solution{
public:
    int minimumNumber(int n,vector<int> &arr){
       int flg=0,ans=INT_MAX;
       for(auto it:arr)
       {
           if(it%2==1)
           {
               flg=1;
               break;
           }
           ans=min(ans,it);
       }
       if(flg)
       return flg;
       return ans;
    }
};

